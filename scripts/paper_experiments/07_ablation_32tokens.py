#!/usr/bin/env python3
"""
Ablation Study: 32 Memory Tokens (Upper Bound)
===============================================

This script trains with 32 memory tokens to test
upper bound of memory capacity benefit.

Output: outputs/paper_exp_07_ablation_32tokens/
"""

import logging
import sys
from pathlib import Path

import torch

sys.path.insert(0, str(Path(__file__).parent.parent.parent / "src"))

from memxlnet.training import TrainingConfig, XLNetRecurrentTrainer

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


def create_config():
    """Create configuration with 32 memory tokens."""
    device = "cuda" if torch.cuda.is_available() else "cpu"
    has_cuda = torch.cuda.is_available()

    config = TrainingConfig(
        model_name="xlnet-base-cased",
        max_seq_length=384,
        doc_stride=64,
        dataset_name="squad_v2",
        train_split="train",
        eval_split="validation",
        cache_dir="./.cache",
        max_train_samples=None,
        max_eval_samples=None,
        use_lazy_loading=False,
        progressive_segments=[2, 4, 6],
        max_n_segs=6,
        # 32 MEMORY TOKENS (large)
        memory_num_tokens=32,
        memory_update="gated",
        memory_init="learned",
        memory_impl="token",
        use_global_softmax=True,
        num_epochs=3,
        train_batch_size=16,  # May need to reduce if OOM
        eval_batch_size=32,
        learning_rate=3e-5,
        weight_decay=0.01,
        warmup_ratio=0.1,
        max_grad_norm=1.0,
        gradient_accumulation_steps=1,
        eval_steps=6000,
        save_steps=10000,
        logging_steps=500,
        output_dir="./outputs/paper_exp_07_ablation_32tokens",
        run_name="paper-ablation-32tokens",
        save_total_limit=3,
        no_answer_threshold=1.5,
        use_any_positive_logic=True,
        device=device,
        fp16=has_cuda,
        warmup_freeze_base_epochs=0,
        warmup_disable_global_softmax_epochs=1,
        warmup_disable_any_positive_epochs=0,
        push_to_hub_on_save=False,
    )
    return config


def print_experiment_info(config):
    """Print experiment information."""
    print("\n" + "=" * 80)
    print("üìä PAPER EXPERIMENT 07: ABLATION - 32 MEMORY TOKENS")
    print("=" * 80)
    print()
    print("üéØ OBJECTIVE:")
    print("   Test upper bound of memory capacity benefit")
    print()
    print("üìã KEY DIFFERENCE:")
    print(f"   ‚Ä¢ Memory tokens: {config.memory_num_tokens} (vs 8 in main)")
    print("   ‚Ä¢ Note: May require more memory/time")
    print()
    print("üíæ OUTPUT:")
    print(f"   ‚Ä¢ Directory: {config.output_dir}")
    print()
    print("=" * 80 + "\n")


def main():
    """Run ablation experiment."""
    print("üöÄ Starting Ablation 07 (32 Memory Tokens)...\n")
    config = create_config()
    print_experiment_info(config)
    trainer = XLNetRecurrentTrainer(config)

    try:
        trainer.train()
        print("\n‚úÖ Experiment completed!")
        print(f"üìÅ Results: {config.output_dir}")
    except KeyboardInterrupt:
        print("\n‚ö†Ô∏è  Interrupted.")
    except Exception as e:
        print(f"\n‚ùå Failed: {e}")
        raise


if __name__ == "__main__":
    main()
